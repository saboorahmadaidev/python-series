#Data Types, Variables & Operators

a = 20
b = 10

#Integer Data Type 
print("Integers:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a/b)
print("a % b =", a % b)

#Float Data Type 
x = 7.5
y = 2.0
print("\n Floats:") 
print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x/y)
print("x % y =", x % y)

#String Data Type
str1 = "Hello"
str2 = "World"
print("\n Strings:")
print("str1 + str2 =", str1 + " " + str2)
