#Python Operators demonstration

# Arithmetic operators: 
a = 7
b = 3
print("Arithmetic operators:")
print("a + b =", a + b)   
print("a * b =", a * b)   
print("a / b =", a / b)  
print("a % b =", a % b)   
print("a // b =", a // b) 
print("a ** b =", a ** b) 

# Comparison operators:
x = 5
y = 5
print("\nComparison operators:")
print("x == y ->", x == y)    
print("x != y ->", x != y)
print("x < y ->", x < y)  
print("x > y ->", x > y)    
print("x <= y ->", x <= y) 
print("x >= y ->", x >= y) 

# Logical operators: 
p = True
q = False
print("\nLogical operators:")
print("p and q ->", p and q) 
print("p or q ->", p or q)   
print("not p ->", not p)   